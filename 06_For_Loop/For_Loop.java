public class For_Loop {
    public static void main(String [] args){

        String name = "Isskander"  ;

        for(int i=0 ; i<10 ;i++){
            System.out.println(name) ;

        }  
        
        for(int i=1 ; i<=7 ; i++){
            System.out.println("Day be: "+ i) ;

            for(int j=1 ; j<9 ;j++){
                System.out.println(" "+ (j+8) + " - " + (j+1+8)) ;
            }
        }


    }


    
}
