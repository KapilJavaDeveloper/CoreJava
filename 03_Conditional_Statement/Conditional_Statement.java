// package 03_Conditional_Statement;

public class Conditional_Statement {

    public static void main(String [] args){
        int x = 20 ;

        if(x>10 && x<=20){
            System.out.println("Hello") ;
        }
        else{
            System.out.println("Bye") ;
        }

        int num1 = 5 ;
        int num2 = 7 ;

        if(num1 > num2){
            System.out.println(num1) ;
        }
        else{
            System.out.println(num2) ;
         }
    }
    
}
